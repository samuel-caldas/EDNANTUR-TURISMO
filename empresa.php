      <p align="justify"><strong><em>Sobre</em></strong></p>
      
      <p>Fundada em 01/07/1994, tendo como principal  área a de fretamento, diversificou suas funções, possuindo hoje varias áreas de  atuação, como viagens para compra em São Paulo e Goiânia, turismo, locação de  ônibus, transporte de funcionários entre outras. <br />
        Possui uma frota com ônibus leito e  semi-leito, ar condicionado, frigobar, gabinete sanitário, CD, DVD, TV tudo  para o conforto e satisfação de seus clientes. </p>
      <p>Desde a sua fundação, as práticas de  negócio da EDNANTUR sempre foram pautadas em atendimento humanizado, manutenção  da integridade e honestidade, negociação justa e total cumprimento de todas as  leis aplicáveis ao segmento de transporte e aluguel de automóveis. </p>
      <p>A empresa mantém um compromisso sério  com a geração de valor agregado em seus serviços, o que pode ser compartilhado  com seus fornecedores, colaboradores e clientes. Tudo isso para tornar a  reputação da EDNANTUR um dos ativos mais importantes da empresa. </p>
<p align="justify">&nbsp;</p>
<p align="justify"><strong><em>Missão</em></strong></p>
      
      <p>Manter o relacionamento humanizado,  saudável e idôneo junto aos clientes; <br />
        Manter a ética e o respeito aos  colaboradores e clientes; <br />
        Superar as expectativas dos clientes; <br />
        Oferecer excelência no atendimento,  honrando prazos e garantindo qualidade na prestação de serviços de transporte  de passageiros e aluguel de Ônibus; <br />
        Promover a responsabilidade social,  ajudando a comunidade na qual está inserida; <br />
        Ser uma empresa voltada para o  crescimento e bem-estar dos seus colaboradores e clientes. </p>
<p align="justify">.</p>
      <p align="justify"><strong><em>Visão</em></strong></p>
<p>Manter e fortalecer a marca no mercado,  atuando com responsabilidade para se destacar entre os mais respeitados  fornecedores de serviços de transporte e aluguel de ônibus do país. </p>
<p align="justify">&nbsp;</p>
      <p align="justify"><strong><em>Valores</em></strong></p>
      <p>Manter a conduta ética em todos os nossos  serviços, primando sempre pela transparência e honestidade entre colaboradores  e parceiros. <br />
        Praticar o empreendedorismo, sempre  imbuídos de responsabilidade social e ambiental. </p>
